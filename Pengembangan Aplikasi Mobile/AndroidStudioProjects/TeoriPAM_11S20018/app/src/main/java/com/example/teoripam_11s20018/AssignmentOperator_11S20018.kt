package com.example.teoripam_11s20018

fun main(args: Array<String>){
    var a:Int = 20
    var b:Int = 15

    a += b
    println("a += b = " + a)
    a -= b
    println("a -= b = " + a)
    a *= b
    println("a *= b = " + a)
    a /= b
    println("a /= b = " + a)
    a %= b
    println("a %= b = " + a)
}