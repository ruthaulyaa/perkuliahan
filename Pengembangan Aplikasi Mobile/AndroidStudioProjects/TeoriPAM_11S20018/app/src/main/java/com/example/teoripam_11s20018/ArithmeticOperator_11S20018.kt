package com.example.teoripam_11s20018

fun main(args: Array<String>){
    var a: Double = 8.0
    var b: Double = 4.0
    var result: Double = 0.0

    result = a + b
    println("a + b =" + result)
    result = a - b
    println("a - b =" + result)
    result = a * b
    println("a * b =" + result)
    result = a / b
    println("a / b =" + result)
    result = a % b
    println("a % b =" + result)

}